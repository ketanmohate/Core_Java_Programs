/* 10.Write a C program to check whether a character is uppercase or lowercase alphabet. */

import java.util.*;

public class P10_character_is_uppercase_or_lowercase_alphabet 
{
	public static void main(String[] args) 
	{
		Scanner sc = new Scanner(System.in);
		
		System.out.println("Enetr the chacter");
		char ch = sc.nextLine().charAt(0);
		
		if(ch >= 'a' && ch <= 'z')
		{
			System.out.println("Character is Lowecase");
		}
		else if(ch >= 'A' && ch <= 'Z')
		{
			System.out.println("Character is uppercase");
		}
		else
		{
			System.out.println("Invalid Input");
		}
		
	}

}
